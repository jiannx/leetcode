/**
 * Definition for singly-linked list.
 * class ListNode {
 *     val: number
 *     next: ListNode | null
 *     constructor(val?: number, next?: ListNode | null) {
 *         this.val = (val===undefined ? 0 : val)
 *         this.next = (next===undefined ? null : next)
 *     }
 * }
 */

function reverseList(head: ListNode | null): ListNode | null {
  let cur = null;
  let pre = head;
  while (pre) {
    let tmp = pre.next;
    pre.next = cur;
    cur = pre;
    pre = tmp;
  }
  return cur;
};
